	<br><br><h1 class="normal"> Resep Gudeg Jogja </h1><br><br>
	<img src="gudegjogja.jpg" width="300px" height="200px" style="display: block; margin: auto;">
	<br><br>
	<p style="text-align: justify; text-indent: 0.5in;"> Gudeg atau yang biasa dikenal dengan sebutan Gudeng Jogja adalah makanan khas Yogyakarta dan Jawa Tengah yang terbuat dari nangka muda yang dimasak dengan santan. Perlu waktu berjam-jam untuk membuat masakan ini. Warna coklat biasanya dihasilkan oleh daun jati yang dimasak bersamaan. Biasanya Gudeg dimakan dengan nasi dan disajikan dengan kuah santan kental, ayam kampung, telur, tempe, tahu, dan sambal goreng krecek. Sebelum memasak Gudeg Jogja ada beberapa bahan - bahan yang perlu disiapkan.</p><br>
	<p > Bahan - Bahan : <br> 1. 1/2 butir kelapa <br> 2. 1/2 Ayam sedang <br> 3. 3 lembar daun jati <br> 4. 1/4 kg Telur ayam <br> 5. 500gr Nangka muda</p><br>
	<p> Bumbu - Bumbu : <br> 1. 3 siung bawang merah <br> 2. 4 siung bawang putih <br> 3. 1 sendok teh ketumbar <br> 4. 2 potong lengkuas <br> 5. 6 butir kemiri <br> 6. 1/4 sendok terasi <br> 7. 3 lembar daun salam <br> 8. 1 sdm garam <br> 9. 1 sdm gula merah dan vitsin </p><br>
	<p  style="text-align: justify;"> Berikut adalah cara membuat Gudeg Jogja : </p>
	<p> 1. Rebuslah telur dan parutlah kelapa, untuk dijadikan santan. <br> 2. Nangka muda dipotong - potong agak kasar setelah dicuci lebih dahulu. <br> 3. Kemudian rebus dengan daun jati (supaya timbul warna merah sehingga lunak). Tiriskan lalu memarkan. <br> 4. Haluskan bumbu, kecuali daun salam dan lengkuas. <br> 5. Masukkan kedalam panci bersama santan, potong ayam, dan nangka muda yang telah memar. <br> 6. Tambahkan daun salam dan lengkuas. Rebus terus hingga santannya habis. <br> 7. Terakhir masukkan telur rebus yang telah dikupas, ditambahkan santan kental dan rebuslah hingga santan habis. </p><br><br>
