	<br><br><h1 class="normal"> Resep Kue Bikang Ketan </h1><br><br>
	<img src="bikangketan.jpg" width="300px" height="200px" style="display: block; margin: auto;">
	<br><br>
	<p style="text-align: justify; text-indent: 0.5in;"> Kue bikang merupakan kue tradisional yang berbahan dasar tepung beras. Kue bikang bisa dijumpai di pasar tradisional. Kue bikang berbentuk seperti bunga merekah dan diberi warna. Rasanya manis dan memiliki tekstur lembut dan berserat. Kue bikang termasuk kue basah yang cara Pembuatannya melewati proses pemanggangan dalam cetakan khusus kue cara bikang. Sebelum memasak Kue Bikang Ketan ada beberapa bahan - bahan yang perlu disiapkan.</p>
	<br>
	<p > Bahan - Bahan : <br> 1. 4 butir telur ayam <br> 2. 1 gelas gula pasir <br> 3. 2 1/2 gelas santan kental <br> 4. 250 gr kentang rebus, diuleg agak kasar <br> 5. Vanili, dan garam secukupnya </p>
	<br>
	<p  style="text-align: justify;"> Cara Membuat Kue Bikang Ketan : </p>
	<p> 1. Santan direbus sampai keluar minyaknya, sambil diaduk-diaduk agar santan tidak pecah. <br> 2. Telur ayam dan gula pasir dikocok sampai mengembang. <br> 3. Kentang yang sudah dihaluskan, masukkan kedalam kocokan telur. <br> 4. Tambahkan pula vanili dan garam. Aduk sampai rata. <br> 5. Adonan tadi tuangkan kedalam cetakan bikang. <br> 6. Terlebih dahulu, bagian tengah cetakan beri kertas minyak sedikit. <br> 7. Setelah itu bakar dan tutup, membakarnya diatas api arang. <br> 8. Bila sudah matang, keluarkan dari cetakan lalu potong-potong menurut selera. <br> 9. Siap dihidangkan.</p><br><br>
