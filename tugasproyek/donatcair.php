	<img src="donatcair.jpg" width="300px" height="200px" style="display: block; margin: auto;">
	<br><br>
	<p style="text-align: justify; text-indent: 0.5in;"> Donat adalah makanan atau jajanan yang digoreng, dibuat dari adonan tepung terigu, gula, telur, dan mentega. Donat yang paling umum adalah donat berbentuk cincin dengan lubang di tengah dan donat berbentuk bundar dengan isian manis, seperti selai, jelly, krim, dan custard.</p>
	<p style="text-align: justify; text-indent: 0.5in;"> Tapi taukah anda bahwa donat cair berbeda, adonan donat ini cair bisa di bilang kental tidak seperti adonan donat pada umumnya. Jadi tidak perlu diuleni atau di banting-banting. Donat cair sangat cocok untuk sarapan di pagi hari, untuk bekal anak sekolah, maupun dihidangkan saat santai bersama keluarga tercinta. Donat cair, selain empuk teksturnya juga lembut dengan taburan gula halus di atasnya, membuat donat ini sangat digemari anak â€“ anak. Sebelum memasak Donat Cair ada beberapa bahan - bahan yang perlu disiapkan.</p>
	<br>
	<p > Bahan - Bahan : <br> 1. 1kg tepung terigu <br> 2. 4 butir telur <br> 3. 1/4 kg gula pasir <br> 4. 900 cc air kelapa <br> 5. 1 sdm gist <br> 6. 1 sdt baking powder <br> 7. 1 ons mentega <br> 8. 1/2 kg minyak goreng <br> 9. 1 ons misis</p>
	<br>
	<p  style="text-align: justify;"> Cara Membuat Donat Cair : </p>
	<p> 1. Gula pasir dan telur dikocok sampai mengembang. <br> 2. Kemudian masukkan air kelapa sedikit demi sedikit. <br> 3. Masukkan tepug terigu bersama baking powder, gist, mentega cair, aduk dampai rata. <br> 4. Diamkan selama 1/4 jam lalu cetak dam goreng dalam minyak yang sudah panas. <br> 5. Tunggu sampai kuning baru angkat. <br> 6. Bila sudah dingin taburi atasnya dengan misis, atau boleh pakai gula halus.</p><br><br>
