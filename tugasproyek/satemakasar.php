	<br><br>
	<p style="text-align: justify; text-indent: 0.5in;"> Sate adalah hidangan yang sangat populer di Indonesia dengan berbagai suku bangsa dan tradisi seni memasak telah menghasilkan berbagai jenis sate. Resep dan cara pembuatan sate beraneka ragam bergantung variasi dan resep masing-masing daerah. Sate disajikan dengan berbagai macam bumbu yang bergantung pada variasi resep sate. Daging yang dijadikan sate antara lain daging ayam, kambing, domba, sapi, babi, kelinci, kuda, dan lain-lain. Hampir segala jenis daging dapat dibuat sate. Sebagai negara asal mula sate, Indonesia memiliki variasi resep sate yang kaya. Salah satunya yaitu Sate Makasar dari Sulawesi Selatan. Sate ini terbuat dari jeroan sapi atau kambing yang dibumbui saus yang terbuat dari belimbing. Rasanya khas asam dan pedas. Tidak seperti sate lainnya, sate Makassar dihidangkan tanpa bumbu. Sebelum memasak Sate Makasar ada beberapa bahan - bahan yang perlu disiapkan.</p>
	<br>
	<p > Bahan - Bahan : <br> 1. 1 kg daging has dalam/lulur dalam <br> 2. 1 kg usus <br> 3. 3 sdm ketumbar <br> 4. 1 sdm jinten <br> 5. 10 biji kemiri <br> 6. 10 buah bawang merah <br> 7. 10 siung bawang putih <br> 8. 2 ruas jari kunyit <br> 9. 2 batang serai, dimemarkan <br> 10. garam secukupnya</p>
	<br>
	<p  style="text-align: justify;"> Cara Membuat Sate Makasar : </p>
	<p> 1. Usus dibersihkan kemudian direbus dan diiris seperti sate. <br> 2. Daging dipotong - potong dadu. <br> 3. Semua bumbu dihaluskan kecuali serai, ditumis sampai baunya harum. <br> 4. Kemudian campurkan semua bahan menjadi satu. <br> 5. Diamkan selama 30 menit. <br> 6. Kemudian, baru ditusukkan dan dibakar sampai matang.</p><br><br>
